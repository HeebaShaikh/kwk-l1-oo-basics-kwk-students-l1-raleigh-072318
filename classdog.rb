class dog 
  def initialize (name, breed)
    @name = name 
    @breed = breed 
  end 
  
  #getter for instance attribute name
  def name 
    @name 
  end 
  
  # getter for instance attribute breed 
  def breed 
    @breed 
  end 
  
  #setter for instance attribute name 
  def name=(name)
    @name = name 
  end 
  
  # setter for instance attribute breed 
  def breed=(breed)
    @breed = breed 
  end 
end 